SCALE = 8  # 20*8=160px wide, 28*8=224px tall
W, H = 20, 28

def render_frame(sprite):
    img = Image.new('RGBA', (W * SCALE, H * SCALE), (0, 0, 0, 0))
    pixels = img.load()
    for y in range(H):
        for x in range(W):
            c = sprite[y][x]
            if c == 0:
                continue
            color = MP[c] + (255,)
            for sy in range(SCALE):
                for sx in range(SCALE):
                    pixels[x * SCALE + sx, y * SCALE + sy] = color
    return img

frame1 = render_frame(dance1)
frame2 = render_frame(dance2)

# Convert RGBA to palette mode with transparency
# Use a unique color not in the sprite as the transparent color
TRANS = (1, 1, 1)  # near-black, not used in palette

def rgba_to_gif_frame(img):
    """Convert RGBA image to P mode with transparency index."""
    # Create a new RGB image with the transparent color as background
    bg = Image.new('RGB', img.size, TRANS)
    # Paste the sprite onto it using the alpha channel as mask
    bg.paste(img, mask=img.split()[3])
    # Convert to palette
    p = bg.quantize(colors=255, method=Image.Quantize.MEDIANCUT)
    return p

p1 = rgba_to_gif_frame(frame1)
p2 = rgba_to_gif_frame(frame2)

# Find the transparency index (the palette index for TRANS color)
palette = p1.getpalette()
trans_idx = None
for i in range(256):
    r, g, b = palette[i*3], palette[i*3+1], palette[i*3+2]
    if (r, g, b) == TRANS:
        trans_idx = i
        break

# Save animated GIF with true transparency
p1.save(
    '/root/milkman/milkman-dance.gif',
    save_all=True,
    append_images=[p2],
    duration=300,
    loop=0,
    disposal=2,
    transparency=trans_idx if trans_idx is not None else 0,
)

